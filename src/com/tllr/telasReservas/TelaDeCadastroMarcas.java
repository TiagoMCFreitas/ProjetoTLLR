 public void imprimirDadosNaGrid(ArrayList<Marca> listaDeMarcas) {
        try {

            DefaultTableModel model = (DefaultTableModel) jTableMarcas.getModel();
            
            

            //Limpa a tabela 
            Collections.sort(listaDeMarcas);
            model.setNumRows(0);
            Iterator<Marca> lista = listaDeMarcas.iterator();               

            while (lista.hasNext()) {
                String[] saida = new String[3];
                Marca aux = lista.next();
                saida[0] = aux.getId() + "";
                saida[1] = aux.getDescricao();
                saida[2] = aux.getUrl();
                
                //Incluir nova linha na Tabela
                model.addRow(saida);
                
            }
            
        } catch (Exception erro) {
            JOptionPane.showMessageDialog(this, erro.getMessage());
        }

    }
    private void jLabelBotaoBuscarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabelBotaoBuscarMouseClicked
         ImageIcon iconeBuscarMouse = new ImageIcon("./src/Imagens/ImageAnimacoes/botaobuscarclicado.png");
        jLabelBotaoBuscar.setIcon(iconeBuscarMouse);    
        try {
            
            JFileChooser fc = new JFileChooser("./src/Imagens/imagesLogo");
            File workingDirectory = new File("./src/Imagens/imagesLogo");
            fc.setCurrentDirectory(workingDirectory);
            fc.setFileSelectionMode(JFileChooser.FILES_ONLY);
            fc.showOpenDialog(this);
            File arquivo = fc.getSelectedFile();
            String nomeDoArquivo = arquivo.getPath();
            jTextFieldUrl.setText(nomeDoArquivo);
            ImageIcon iconLogo = new ImageIcon(nomeDoArquivo);
            jLabelLogo.setIcon(iconLogo);
        } catch (Exception erro) {
            JOptionPane.showMessageDialog(this,"Nenhum arquivo selecionado");
        }

    }//GEN-LAST:event_jLabelBotaoBuscarMouseClicked

