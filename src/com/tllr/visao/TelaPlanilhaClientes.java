    public void imprimirDadosNaGrid(ArrayList<Cliente> listaDeClientes){
         
            DefaultTableModel model = (DefaultTableModel) jTableClientes.getModel();
            //Limpa a tabela 
            model.setNumRows(0);
            Iterator<Cliente> lista = listaDeClientes.iterator();               

            while (lista.hasNext()) {
             
                saida = new String[16];
                Cliente aux = lista.next();
                saida[0] = aux.getId() + "";
                saida[1] = aux.getCpf_cnpj();
                saida[2] = aux.getNome();
                saida[3] = aux.getRazaoSocial();
                saida[4] = aux.getIdentidade()+ "";
                saida[5] = "+"+aux.getTelefone().getDDI() + "("+aux.getTelefone().getDDD() +")" + aux.getTelefone().getNumero()+"";
                saida[6] = aux.getEmail();
                saida[7] = aux.getEndereco().getCEP() +";"+ aux.getEndereco().getBairro() +";"+  aux.getEndereco().getCidade() 
                        +";"+ aux.getEndereco().getComplemento() +";"+ aux.getEndereco().getEstado() +";"+ aux.getEndereco().getLogradouro();
                saida[8] = aux.getTipoCliente().toString();
                int i = 0;
               
                //Incluir nova linha na Tabela
                model.addRow(saida);
                
            }
         
    }
