    private void jLabelBotaoAlterar2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabelBotaoAlterar2MouseClicked
        if(jTableClientes.getSelectedRow() != - 1){
            this.dispose();

            cliente = new TelaAlterarCliente();
            String[] itensTable = new String[9];

            itensTable[0] = jTableClientes.getValueAt(jTableClientes.getSelectedRow(), 0).toString();
            itensTable[1]= jTableClientes.getValueAt(jTableClientes.getSelectedRow(),1).toString();
            itensTable[2] = jTableClientes.getValueAt(jTableClientes.getSelectedRow(),2).toString();
            itensTable[3] = jTableClientes.getValueAt(jTableClientes.getSelectedRow(),3).toString();
            itensTable[4] =  jTableClientes.getValueAt(jTableClientes.getSelectedRow(),4).toString();

            itensTable[5]  = jTableClientes.getValueAt(jTableClientes.getSelectedRow(),5).toString();
            String[] dividir = itensTable[5].split("");
            String telefone = dividir[7] + dividir[8] + dividir[9] + dividir[10] + dividir[11] + dividir[12] + dividir[13] + dividir[14] + dividir[15];

            itensTable[6] =  jTableClientes.getValueAt(jTableClientes.getSelectedRow(),6).toString();
            itensTable[7] =  jTableClientes.getValueAt(jTableClientes.getSelectedRow(),8).toString();

            itensTable[8] =  jTableClientes.getValueAt(jTableClientes.getSelectedRow(),7).toString();
            String[] endereco = itensTable[8].split(";");

            cliente.receber(itensTable[0],itensTable[1], itensTable[2], itensTable[3], itensTable[4], dividir[1] + dividir[2] , dividir[4] + dividir[5], telefone,itensTable[6], TipoCliente.valueOf(itensTable[7]));
            cliente.manterEndereco(endereco[0],endereco[1],endereco[2],endereco[3],endereco[4],endereco[5]);
            cliente.receberEpor();
            cliente.setVisible(true);
        }
        else{
            JOptionPane.showMessageDialog(this, "Nenhum cliente sendo alterado, clique em algum na tabela para prosseguir");
        }
    }//GEN-LAST:event_jLabelBotaoAlterar2MouseClicked

