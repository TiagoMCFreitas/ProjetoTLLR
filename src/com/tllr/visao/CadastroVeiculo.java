    private void jComboBoxModelosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBoxModelosActionPerformed
        try {
            ArrayList<Modelo> listaModelos = modelo.listagemModelo();
            for(int i = 0; i < listaModelos.size(); i++){
                if(jComboBoxModelos.getSelectedItem().equals(listaModelos.get(i).getDescricao())){
                    String iconModelo = listaModelos.get(i).getUrl();
                    String iconMarca = listaModelos.get(i).getMarca().getUrl();
                    ImageIcon modelo = new ImageIcon(iconModelo);
                    ImageIcon marca = new ImageIcon(iconMarca);
                    modelo.setImage(modelo.getImage().getScaledInstance(jLabelModelo.getWidth(), jLabelModelo.getHeight(), 1));
                    marca.setImage(marca.getImage().getScaledInstance(jLabelMarca.getWidth(), jLabelMarca.getHeight(), 1));
                    jLabelModelo.setIcon(modelo);
                    jLabelMarca.setIcon(marca);
                }
            }
        }catch(Exception e){
                    
                    }
    }//GEN-LAST:event_jComboBoxModelosActionPerformed
